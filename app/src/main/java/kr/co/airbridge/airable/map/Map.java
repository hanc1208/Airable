package kr.co.airbridge.airable.map;

public class Map {
    private int id;
    private Vertex[] vertexes;
    private int width;
    private int height;

    public int getId() {
        return id;
    }

    public Vertex[] getVertexes() {
        return vertexes;
    }

    public int getWidth() {
        return width;
    }

    public int getHeight() {
        return height;
    }
}
